public class PrintAsciiValueExample1{

	public static void main(String[] args){
	
		char a='A';
		char b='a';
		
		int ascii_A=a;
		int ascii_a=b;
		
		System.out.println("ASCII value of A - "+ascii_A );//65
		System.out.println("ASCII value of a - "+ascii_a );//97
	}
}
